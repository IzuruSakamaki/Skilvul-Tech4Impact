/*
1. Encapsulation 
    >> Cara untuk membatasi akses langsung ke properti atau method dari sebuah objek. 
        Hal ini dimaksudkan untuk membungkus data variabel dan fungsi menjadi satu kesatuan.
    >> Dibutuhkan untuk mencegah modifikasi data yang tidak disengaja, karena variabel objek 
        pada data hanya dapat di akses melalui method, sehingga data akan tetap terjaga. 
2. Abstraction 
    >> Konsep untuk menyembunyikan suatu detail proses yang tidak perlu diketahui oleh user. 
        Contohnya ketika user menggunakan sebuah smartphone, user tidak perlu memahami apa yang 
        terjadi di dalam logika elektronik smartphone tersebut. User hanya perlu menekan tombol 
        power dan menyentuh layar agar alat tersebut bisa bekerja dengan semestinya. 
    >> Dibutuhkan untuk menyembunyikan detail tertentu dari sebuah objek dan menampilkan 
        fungsionalitas atau fitur penting dari objek tersebut.
3. Inheritance 
    >> Sebuah konsep dimana sebuah class menurunkan atau mewariskan properti dan method pada 
        class lain atau childnya, dengan syarat harus mempunyai parent class. Untuk dapat menerima 
        dari turunan parent class adalah dengan menggunakan kata kunci extends pada child class 
        diikuti oleh nama parent classnya. 
    >> Dibutuhkan karena sesuai dengan konsep javascript yang memegang prinsip DRY (Don't Repeat Yourself),
        yang artinya akan berguna ketika pada sebuah kode memiliki kesalahan yang sama dan terus berulang.
4. Polymorpishm 
    >> Kemampuan untuk mengambil bentuk yang berbeda. Sesuai dengan asal katanya yaitu poly yang 
        berarti banyak dan morphe berarti bentuk, artinya variabel, fungsi, atau objek yang sama 
        dapat digunakan pada tipe yang berbeda.
    >> Dibutuhkan karena dapat membentuk banyak atribut dengan baik, menumbuhkan modularitas dan usabilitas, 
        serta membuat jenis sistem yang lebih fleksibel dan mudah dibentuk.
*/